-- ============================================================
-- 016_agencies_features.sql
-- agencies テーブルに「この相談所の特徴」フィールドを追加
-- ============================================================
-- 目的：
-- ユーザー画面の /agencies/[id] に「この相談所の特徴」セクションがあるが、
-- カウンセラー管理画面で編集する場所が無かった。features text[] を追加して
-- 相談所オーナーが自由なタグで特徴を登録できるようにする。
--
-- 例: ['専任カウンセラー制', 'IBJ加盟', 'オンライン対応可', '個別最適化']
--
-- 使い方：Supabase ダッシュボード > SQL Editor で本ファイルを実行。
-- （または apply_migration MCP 経由）
-- ============================================================

alter table public.agencies
  add column if not exists features text[] not null default '{}';

comment on column public.agencies.features is
  'この相談所の特徴（フリーテキストのタグ配列）。お客様画面の詳細「特徴」セクションに表示される。';
