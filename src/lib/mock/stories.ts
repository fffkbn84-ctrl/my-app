  title: string;
  /** 投稿者の表記（例: "A.M さん"） */
  author: string;
  /** 表示用の年齢（例: "32歳"） */
  age: string;
  /** フィルタ用の年代帯 */
  ageBand: StoryAgeBand;
  /** 例: "6ヶ月で成婚" */
  status: string;
  /** 現在のステージ（フィルタ用） */
  stage: StoryStage;
  /** 投稿月 */
  date: string;
  /** 例: "出会いから6ヶ月"。フィルタ用に月数も持つ */
  periodLabel: string;
  periodMonths: number;
  /** 担当カウンセラー・所属相談所（既存 data.ts と紐づく） */
