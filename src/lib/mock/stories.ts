  counselorName: string;
  agencyId: number;
  agencyName: string;
  /** 詳細ページ本文（段落配列） */
  body: string[];
  /** 中間の引用（任意） */
  pullQuote?: string;
  /** タグ */
  tags: string[];
  /** 共感カウント（モック） */
  sympathyCount: number;
  /** カードのアクセントカラー（淡いグリーン系） */
  accentColor: string;
  accentSoft: string;
  /** カードのサムネ画像（任意・未指定なら stage 別フォールバック）。
   *  同一 stage の物語が並ぶと絵が重複するため、物語ごとに上書きできる。 */
  thumbnail?: string;
  /** 質問形 Q&A（任意）。詳細ページの FAQPage 構造化データに使う（AEO/リッチリザルト） */
  faq?: { q: string; a: string }[];
